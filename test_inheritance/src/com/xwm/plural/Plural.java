package com.xwm.plural;

public class Plural {
	private double  a;
	private double b;
	public Plural() {
		
		
	}
	public Plural(double a, double b) {
		super();
		this.a = a;
		this.b = b;
	}
	public double getA() {
		return a;
	}
	public void setA(double a) {
		this.a = a;
	}
	public double getB() {
		return b;
	}
	public void setB(double b) {
		this.b = b;
	}
	
	

}
